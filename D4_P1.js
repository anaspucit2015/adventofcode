const checkUpward = (x, y) => {
  findAndJoinValues([
    [x, y],
    [x - 1, y],
    [x - 2, y],
    [x - 3, y]
  ])
}

const checkDownWard = (x, y) => {
  findAndJoinValues([
    [x, y],
    [x + 1, y],
    [x + 2, y],
    [x + 3, y]
  ])
}

const checkForward = (x, y) => {
  findAndJoinValues([
    [x, y],
    [x, y + 1],
    [x, y + 2],
    [x, y + 3]
  ])
}

const checkBackward = (x, y) => {
  findAndJoinValues([
    [x, y],
    [x, y - 1],
    [x, y - 2],
    [x, y - 3]
  ])
}

const checkUpdigonal = (x, y) => {
  findAndJoinValues([
    [x, y],
    [x - 1, y - 1],
    [x - 2, y - 2],
    [x - 3, y - 3]
  ])
}

const checkDownDiagonal = (x, y) => {
    findAndJoinValues([
      [x, y],
      [x + 1, y + 1],
      [x + 2, y + 2],
      [x + 3, y + 3]
    ])
  }

const checkReverseDownDiagonal = (x, y) => {
  findAndJoinValues([
    [x, y],
    [x + 1, y - 1],
    [x + 2, y - 2],
    [x + 3, y - 3]
  ])
}

